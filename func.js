function calculate(num1,num2,operator){
    // const operator = ("+","-","/","*");
    if(operator == "+"){
        
        console.log(num1 + num2)
    }
    if(operator == "-"){
        console.log(num1 - num2)
    }
    
    if(operator == "/"){
        console.log(num1 / num2)
    }
    if(operator == "*"){
        console.log(num1 * num2)
    }
}
calculate(1,0,"/");
